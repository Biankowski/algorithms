# A Pilha é uma estrutura de dados que funciona literalmente como uma pilha.
# Você adiciona elementos em cima dos outros, e pode adicionar ou remover elementos.
# Uma pilha é conhecida como uma estrutura LIFO (last in, first out).
# Um exemplo disso pode ser uma pilha de pratos. Você vai adicionando um em cima do outro, e o último a ser adicionado será o primeiro a ser retirado da pilha.
# Um exemplo prático dessa estrutura de dados é o histórico do navegador. Você acessa sites e tem a opção de retornar pra a última página que você acessou.

# Acidionar/Remover elementos tem a complexidade de tempo constante O(1).
# Procurar elementos tem a complexidade de O(n)

# A chamada de funções nas linguagens de programação funcionam em cima de uma pilha. Vai chamando uma função em cima da outra.
# A funcionalidade ctrl+z nos editores também funciona como uma pilha. Ao apertar crtl+z o editor volta para a última configuração registrada.
